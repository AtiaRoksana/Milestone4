const color = 'red';
if (color === 'green') {
    console.log('you are green friend');
}

if (color === 'blue') {
    console.log('you are blue friend');
}

if (color === 'red') {
    console.log('you are red friend');
}

if (color === 'white') {
    console.log('you are white friend');
}

if (color === 'yellow') {
    console.log('you are yellow friend');
}

else {
    console.log('you are a black  friend');
}

//switch
switch (color) {
    case 'green':
        console.log('green friend');
        break;
    case 'blue':
        console.log('blue friend');
        break;
    case 'red':
        console.log('red friend');
        break;
    case 'white':
        console.log('white friend');
        break;

    default:
        console.log('black friend');
        break;
}
